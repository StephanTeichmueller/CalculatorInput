





namespace OwnMathLib
{
    public class AritmethicOperations
    {
        public double x {  get; set; }
        public double y { get; set; }

        public double Add()
        {
            return x + y;
        }

        public double Sub()
        {
            return x - y;
        }
        public double Mul()
        {
            return x * y;
        }
        public double Div()
        {
            return x / y;
        }
    }
}